export default function BlogPost() {
  const router = useRouter()
  const { id } = router.query

  const post = blogPosts.find(p => p.id === Number(id))

  if (!post) {
    return <div>文章未找到</div>
  }

  return (
    <div className="min-h-screen bg-gray-100">
      <Head>
        <title>{post.title} - 云数联算科技技术博客</title>
        <meta name="description" content={post.content.slice(0, 150)} />
      </Head>

      <Header />

      <main className="container mx-auto px-4 py-8">
        <article className="max-w-3xl mx-auto bg-white p-8 rounded-lg shadow-md">
          <div className="mb-8">
            <h1 className="text-3xl font-bold text-blue-600 mb-4">{post.title}</h1>
            
            <div className="flex items-center mb-6 text-gray-600">
              <img 
                src={post.authorAvatar} 
                alt={post.author} 
                className="w-12 h-12 rounded-full mr-4 object-cover"
              />
              <div>
                <div>
                  <span className="font-semibold">{post.author}</span>
                  <span className="mx-2">|</span>
                  <span>{post.date}</span>
                  <span className="mx-2">|</span>
                  <span>{post.readTime}</span>
                </div>
                <div className="flex space-x-2 mt-2">
                  {post.tags.map((tag) => (
                    <span 
                      key={tag} 
                      className="bg-blue-100 text-blue-600 px-2 py-1 rounded-full text-xs"
                    >
                      {tag}
                    </span>
                  ))}
                </div>
              </div>
            </div>
          </div>

          <div 
            className="prose prose-blue max-w-none"
            dangerouslySetInnerHTML={{ __html: convertMarkdownToHTML(post.content) }}
          />
        </article>
      </main>

      <Footer />
    </div>
  )
}

// 简单的 Markdown 转 HTML 转换函数（实际项目中建议使用 remark 或 marked 库）
function convertMarkdownToHTML(markdown: string) {
  return markdown
    .replace(/^## (.*$)/gim, '<h2 class="text-2xl font-bold text-blue-600 mt-8 mb-4">$1</h2>')
    .replace(/^### (.*$)/gim, '<h3 class="text-xl font-semibold text-blue-500 mt-6 mb-3">$1</h3>')
    .replace(/\*\*(.*?)\*\*/g, '<strong>$1</strong>')
    .replace(/^> (.*$)/gim, '<blockquote class="border-l-4 border-blue-500 pl-4 italic text-gray-600 my-4">$1</blockquote>')
    .replace(/\n/g, '<br />')
} 